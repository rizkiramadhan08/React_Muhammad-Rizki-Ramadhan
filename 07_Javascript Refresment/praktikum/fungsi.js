// Deklarasi fungsi
function greetUser(name) {
    return `Hello ${name}! ,contoh penggunaan greetUser`;
}

// Arrow function
const greetUserArrow = (name) => {
    return `Hello  ${name}! ,contoh penggunaan grearUser Arrow`;
};

// Contoh penggunaan
console.log(greetUser('Rizki'));       // Output: Hello Rizki! ,contoh penggunaan greetUser
console.log(greetUserArrow('Rizki')); // Output:  Hello  Rizki! ,contoh penggunaan grearUser Arrow
